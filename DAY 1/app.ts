// Pre-increment Example
let x: number = 5;
let y: number = ++x; // Pre-increment
console.log(x); // Output: 6
console.log(y); // Output: 6

// Post-increment Example
let a: number = 5;
let b: number = a++; // Post-increment
console.log(a); // Output: 6
console.log(b); // Output: 5
